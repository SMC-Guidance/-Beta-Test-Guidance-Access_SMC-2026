/* Parse the raw pipe-delimited text into structured sections/students. */
SMC.classListData = (function () {
	var raw = SMC.classListRaw || "";
	var lines = raw.split(/\r?\n/);
	var sections = [];
	var cur = null;
	for (var i = 0; i < lines.length; i++) {
		var line = lines[i];
		if (!line || !line.trim()) continue;
		var parts = line.split("|");
		var tag = parts[0];
		if (tag === "#") {
			cur = {
				level: (parts[1] || "").trim(),
				section: (parts[2] || "").trim(),
				adviser: (parts[3] || "").trim(),
				room: (parts[4] || "").trim(),
				count: parseInt((parts[5] || "0").trim(), 10) || 0,
				students: [],
			};
			if (cur.room === "-" || cur.room === "") cur.room = "";
			sections.push(cur);
		} else if (tag === "S" && cur) {
			cur.students.push({
				sex: (parts[1] || "").trim(),
				lrn: (parts[2] || "").trim(),
				status: (parts[3] || "").trim(),
				name: (parts[4] || "").trim(),
				flag: (parts[5] || "").trim(),
				note: (parts[6] || "").trim(),
			});
		}
	}
	return sections;
})();
